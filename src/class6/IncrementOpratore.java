package class6;

public class IncrementOpratore {
    public static void main(String[] args) {
        int num=10;
        num=num+1;
        System.out.println(num);
        num+=1;
        System.out.println(num);
        num ++;// the same incrementsthe num by 1//
        System.out.println(num);
        int num2=10;
        num2--;
        System.out.println(num2);
    }
}
