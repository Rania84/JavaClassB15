package class6;

         public class WhileLoop2 {
             public static void main(String[] args) {

                 System.out.println("**");
                 int number1 = 5;
                 while (number1 <= 50) {
                     System.out.println(number1);
                     number1 += 5;
                 }
                 System.out.println("**");
                 int number2 = 1;
                 while (number2 < 16) {
                     System.out.println(number2);
                     number2 += 2;
                 }
                 System.out.println("**");
                 int number3 = 1;
                 while (number3 < 101) {
                     System.out.println(number3);
                     number3 += 1;
                 }
             }
         }

